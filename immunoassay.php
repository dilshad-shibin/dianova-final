                            <h2>Immunoassay</h2>
                            <div class="text">
                                <p>An immunoassay is a biochemical test that measures the presence or concentration of a macromolecule or a small molecule in a solution through the use of an antibody (usually) or an antigen (sometimes). The molecule detected by the immunoassay is often referred to as an "analyte" and is in many cases a protein, although it may be other kinds of molecules, of different sizes and types, as long as the proper antibodies that have the required properties for the assay are developed. Analytes in biological liquids such as serum or urine are frequently measured using immunoassays for medical and research purposes.</p>
                                <p class="mb-30">Immunoassays come in many different formats and variations. Immunoassays may be run in multiple steps with reagents being added and washed away or separated at different points in the assay. Multi-step assays are often called separation immunoassays or heterogeneous immunoassays. Some immunoassays can be carried out simply by mixing the reagents and sample and making a physical measurement. Such assays are called homogeneous immunoassays, or less frequently non-separation immunoassays.</p>
                                <p class="mb-30">The use of a calibrator is often employed in immunoassays. Calibrators are solutions that are known to contain the analyte in question, and the concentration of that analyte is generally known. Comparison of an assay's response to a real sample against the assay's response produced by the calibrators makes it possible to interpret the signal strength in terms of the presence or concentration of an analyte in the sample.</p>
                                <p>A wide range of medical tests are immunoassays, called immunodiagnostics in this context. Many home pregnancy tests are immunoassays, which detect the pregnancy marker human chorionic gonadotropin. Other clinical immunoassays include tests that measure levels of CK-MB to assess heart disease, insulin to assess hypoglycemia, the prostate-specific antigen to detect prostate cancer, and some are also used for the detection and/or quantitative measurement of some pharmaceutical compounds.</p>
